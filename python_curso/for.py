"""
         Ciclo For
No es mas que un ciclo foreach, es decir que 
utilizando el ciclo for, seremos capaces de iterar
sobre cada uno de los elementos de una colecci√≥n o un objeto 
iterable, que puede ser, un string, una lista, una tupla, un 
diccionario,etc
"""
usuarios = ["usuario1", "usuario2", "usuario3"]

for usuario in usuarios:
    print(usuario)


